    private void DevamEtBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DevamEtBtnActionPerformed
        
        for(int i=1;i<=muslukSayisi.length;i++){
            musluklar.add(new musluk(i));
        }
        for(int i=0;i<baglantilarTable.getRowCount();i++){
            int musluk1=(int) baglantilarModel.getValueAt(i,0);
            int musluk2=(int) baglantilarModel.getValueAt(i,1);
            int kapasite=(int) baglantilarModel.getValueAt(i,2);
            baglantiEkle(musluk1, musluk2, kapasite);
            baglantiEkle(musluk2, musluk1, kapasite);
            System.out.println(musluk1+" - "+musluk2 +"= "+ kapasite);
        }
        
        for(musluk i:musluklar){
            System.out.println("Musluk "+i.id);
            for(int j=0;j<i.baglanti.size();j++){
                System.out.println("    "+i.baglanti.get(j).id +"Kapasite:"+i.kapasite.get(j));
            }    
        }
        ekran Ekran=new ekran();
        Ekran.setMusluklar(musluklar);
        Ekran.setVisible(true);
        
    }//GEN-LAST:event_DevamEtBtnActionPerformed
    public void baglantiEkle(int Musluk1ID,int Musluk2ID,int Kapasite){
            musluk Musluk2 = null;
            for(musluk i: musluklar){
                if(i.id==Musluk2ID){
                    Musluk2=i;
                }
            }
            
            for(musluk i: musluklar){
                if(i.id==Musluk1ID){
                    i.baglanti.add(Musluk2);
                    i.kapasite.add(Kapasite);
                }
            }
    }
