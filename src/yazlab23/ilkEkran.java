    private void MuslukSecComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MuslukSecComboBoxActionPerformed
       int secilen =MuslukSecComboBox.getSelectedIndex();
       secilen++;
       muslukListeleModel.setRowCount(0);
       muslukSayisi=new int[secilen];
       for(int i=0;i<secilen;i++){
           String muslukAdi=(i+1)+". musluk";
           Object[] x={i+1,muslukAdi};
           muslukListeleModel.addRow(x);
           MusluklarcomboBox.addItem(i+1);
       }
       Musluk1Column.setCellEditor(new DefaultCellEditor(MusluklarcomboBox));
       Musluk2Column.setCellEditor(new DefaultCellEditor(MusluklarcomboBox));
       MuslukListelejPanel.setVisible(true);
       baglantilarModel.setRowCount(0);
       BaglantilarJpanel.setVisible(true);
     
    }//GEN-LAST:event_MuslukSecComboBoxActionPerformed

